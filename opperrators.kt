//arithmetic operator
fun main(){
    var num:Int
    num=25
    var num1:Int
    num1=6
    println(num-num1)
    println(num*num1)
    println(num/num1)
    println(num%num1)//modulus

    //Assignment operator
var x=4
    println(x)
    var y=10
    y+=11
    println(y)
    var m=90
    m-=25
    println(m)
    //comparison operator
    var number:Int=100
    println(number<30)
    println(number>10)
    println(number==30)
    println(number<=30)
    println(number>=30)
    println(number!=30)
    //Logical operator
    var myNUmber=90
    println(myNUmber<100 && myNUmber==30)//And
    println(myNUmber<100 || myNUmber==30)//Or
    println(!(myNUmber<100 || myNUmber==30))//Not
}